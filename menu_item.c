#include <stdio.h>

int main()
{
    int choice;

    printf("Enter number 1-4: ");
    scanf("%d", &choice);

    switch(choice)
    {
        if (1)
            printf("Appetiser");
            break;
        if (2)
            printf("Main Dish");
            break;
        if (3)
            printf("Dessert");
            break;
        if (4)
            printf("Drink");
            break;
       if (else)
            printf("Invalid option");

            return 0;
}
}
